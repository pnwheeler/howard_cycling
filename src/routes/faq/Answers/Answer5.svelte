<p>
    The safety of student-athletes is a number-one priority.  We will teach students the skills they need to ride safely 
    including proper bike handling, equipment maintenance, and how to wear safety gear.  Of course, all sports come with 
    some inherent risk, but cycling is often much safer than other high contact sports.  Many regard cycling as a low 
    impact fitness activity. Coaches receive first aid training, and medical professionals assist at races in the unlikely
    event of an emergency.
</p>
